#include <unistd.h>
#include <stdlib.h>

static size_t	ft_strlen(const char *s)
{
	unsigned int i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	return (i);
}

void			ft_putstr_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s));
}
